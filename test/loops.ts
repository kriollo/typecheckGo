// Test loops

// For loop
for (let i = 0; i < 5; i) {
    console.log(i);
}

// While loop
let counter = 0;
while (counter < 5) {
    console.log(counter);
}

// For loop with array
const numbers = [1, 2, 3, 4, 5];
for (let i = 0; i < 5; i) {
    const num = numbers[i];
    console.log(num);
}

console.log("Loops test completed");
